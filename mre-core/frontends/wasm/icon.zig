// Pleasant-ish dark backgrounds so the white glyph reads; chosen by codepoint.
const BG = [8][3]u8{
    .{ 0x3a, 0x4a, 0x6a }, .{ 0x6a, 0x3a, 0x4a }, .{ 0x3a, 0x6a, 0x4a }, .{ 0x5a, 0x4a, 0x6a },
    .{ 0x6a, 0x55, 0x30 }, .{ 0x30, 0x55, 0x6a }, .{ 0x55, 0x30, 0x55 }, .{ 0x40, 0x40, 0x48 },
};

fn glyph(cp: u32) ?*const [7]u8 {
    if (cp >= '0' and cp <= '9') return &DIGITS[cp - '0'];
    const u = if (cp >= 'a' and cp <= 'z') cp - 32 else cp;
    if (u >= 'A' and u <= 'Z') return &LETTERS[u - 'A'];
    return null;
}

/// Render `cp` as a `size`x`size` RGBA pixel-art icon into `out` (size*size*4 bytes).
pub fn renderLetter(cp: u32, out: [*]u8, size: u32) void {
    const bg = BG[cp % BG.len];
    const g = glyph(cp);
    const scale = @max(@as(u32, 1), size / 8);
    const gw = GW * scale;
    const gh = GH * scale;
    const ox = if (size > gw) (size - gw) / 2 else 0;
    const oy = if (size > gh) (size - gh) / 2 else 0;

    var y: u32 = 0;
    while (y < size) : (y += 1) {
        var x: u32 = 0;
        while (x < size) : (x += 1) {
            var on = false;
            if (g) |rows| {
                if (x >= ox and x < ox + gw and y >= oy and y < oy + gh) {
                    const gx = (x - ox) / scale;
                    const gy = (y - oy) / scale;
                    on = (rows[gy] >> @as(u3, @intCast(GW - 1 - gx))) & 1 == 1;
                }
            }
            const i = (y * size + x) * 4;
            if (on) {
                out[i] = 0xff;
                out[i + 1] = 0xff;
                out[i + 2] = 0xff;
            } else {
                out[i] = bg[0];
                out[i + 1] = bg[1];
                out[i + 2] = bg[2];
            }
            out[i + 3] = 0xff;
        }
    }
}
